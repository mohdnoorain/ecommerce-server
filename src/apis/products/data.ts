// Helper function to get category ID by name (for seeding products)
export const getCategoryIdByName = (categoryName: string, categoriesWithIds: any[]): string => {
  const category = categoriesWithIds.find(cat => cat.name === categoryName);
  return category?._id || category?.id;
};

// Helper function to add categoryId to products after categories are created
export const addCategoryIdsToProducts = (products: any[], categoriesWithIds: any[]): any[] => {
  return products.map(product => {
    // Find the first matching category based on tags
    const matchingCategory = categoriesWithIds.find(category => 
      product.tags.some(tag => category.name.toLowerCase().includes(tag.toLowerCase()))
    );
    
    return {
      ...product,
      categoryId: matchingCategory?._id || matchingCategory?.id
    };
  });
};
